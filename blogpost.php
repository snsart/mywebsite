							<p>
								唐代诗坛虽被历代学者反复耕耘，但聚光灯常照在那些大诗人和名诗人身上，有些诗人至今仍被冷落在“灯火阑珊处”，有些史实仍沉没在历史的深渊中，似乎“世间原未有斯人”。这也许显示了历史的公证，谁给民族和人类的贡献越多越大，谁的英名就传得越久越远；同时这也许显示了人类的精明，不传诵那些名人名作，难道还要去死记三流乃至末流的诗人诗篇吗？对于普通读者来说，用有限的时间来欣赏名人名作无疑是最经济的选择，对于学术研究而言，只把眼光盯着几棵孤零零的参天大树，而无视周围广袤的森林，我们就无法了解那个时代的创作生态，无法认识那个时代的精神氛围，自然也就无法知道参天大树的生长环境，更无法深刻理解和鉴赏大树本身。因为任何诗人都不会是“冰山上的来客”，他们都属于各自时代整体的一部分，在李白和杜甫身旁围绕着一个庞大的诗人群体，他们有着相近的人生理想，相近的情趣爱好，相近的审美体验，还有着相近的语言习惯，相近的诗歌笔法，只有熟悉了这个诗人群体，我们才会对李白和杜甫有“同情之理解”，才深深懂得李杜“原来如此”。
							</p>
							<p>
								在群星灿烂的唐诗星空上，该著所考论的“三包”父子——包融与其二子包何、包佶，自然算不上光彩夺目的明星，但也不是可有可无的无名之辈。包融早在神龙中便“扬名于上京”，初盛唐之际与张若虚、贺知章、张旭并称“吴中四士”。包何、包佶兄弟同样诗名早著，史称“纵声雅道，齐名当时”。可惜，他们在历史上的地位和成就，与今天对他们的重视和研究毫不相称。作者在该书的绪论中说，“从生平考辨来看，除包佶生平经由蒋寅、张强等学者的考证梳理而略为清晰之外，包融、包何的生平尚未得到全面细致的梳理考证。自著作整理而言，除包融诗歌被收入王启兴、张虹所撰《贺知章 包融 张旭 张若虚诗注》外，包何、包佶的诗文尚未得到系统的整理”。眼前这部《唐代“三包”考辨》，在一定程度上弥补了这种学术遗憾。该著首次对包融和包何的生平进行了全面细致的梳理考证，探讨了“三包”家世源流，绘制了“丹阳包氏家族世系表”，总结出丹阳包氏家学家风的特点，并对“三包”交游状况做了详细分类考证，还首次对“三包”诗文进行了全面蒐辑、考辨、编年、校对和注释，并编成《“三包”诗文编年校注》。
							</p>
